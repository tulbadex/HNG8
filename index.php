    window.addEventListener('load', (event) => {
        let contactSubmit = document.getElementById("submit");
        contactSubmit.addEventListener('click',submitContact);
    });

    function submitContact(e) {
        e.preventDefault();
        let name = document.getElementById("InputName").value;
        let email = document.getElementById("InputEmail").value;
        let subject = document.getElementById("InputSubject").value;
        let message = document.getElementById("InputMessage").value;
        let phone = document.getElementById("InputPhone").value;
        let contact = document.getElementById("submit");
        let success = document.getElementById("messageDiv");
        let form = document.getElementById("form");

        if (name != "" && email != "" && subject != "" && message != "") {
            contact.disabled = true
            contact.style.opacity = "0.1"
            fetch('action/action.php', {
                method: 'POST',
                body: JSON.stringify({
                    name: name,
                    email: email,
                    phone: phone,
                    subject: subject,
                    message: message,
                    action: "send"
                }),
                headers:{
					'Accept': 'application/json',
                    'Content-Type': 'application/json'
                }
            }).then( res => res.json())
            .then( data => {
                console.log({data})
                if (data.msg == "success") {
                    contact.disabled = false
					contact.style.opacity = "6"
                    success.innerHTML = "Thanks for contacting us"
                    success.classList.add('text-success')
                    setTimeout(() => {
                        success.innerHTML = ""
                        success.classList.remove("text-success")
                    }, 3000);
                    form.reset()
                }else{
                    success.innerHTML = "Error while sediing message"
                    success.classList.add('text-danger')
                    contact.disabled = false
					contact.style.opacity = "6"
                    setTimeout(() => {
                        success.innerHTML = ""
                        success.classList.remove("text-danger")
                    }, 3000);
                }
            }).catch( err => console.log({ err }))
        }
    }
</script>

